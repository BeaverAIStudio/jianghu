// 合并商店道具中文
window._mergeSectShopLang = function() {
  if (typeof SECT_CONTRIB_SHOP === "undefined" || !SECT_CONTRIB_SHOP) return;
  var L = window.LANG_ZH && window.LANG_ZH.sectShop;
  if (!L) return;
  for (var sid in SECT_CONTRIB_SHOP) {
    var shop = SECT_CONTRIB_SHOP[sid];
    if (shop && shop.items) {
      shop.items.forEach(function(item) {
        if (L[item.id]) Object.assign(item, L[item.id]);
      });
    }
  }
};
window._mergeSectShopLang();
